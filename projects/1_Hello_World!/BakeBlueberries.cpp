// A program that prints out a list of ingredients and instructions
// to for a novice baker to bake blueberry muffins

#include <iostream>

int main()
{
    // Ingredients List
    std::cout << "Ingredients:\n";
    std::cout << "\n";
    std::cout << "1½ cups (190g) all-purpose flour\n";
    std::cout << "¾ cup (150g) sugar\n";
    std::cout << "½ teaspoon salt\n";
    std::cout << "2 teaspoons baking powder\n";
    std::cout << "⅓ cup (80ml) vegetable oil\n";
    std::cout << "1 egg\n";
    std::cout << "⅓ – ½ cup (80–120ml) milk (adjust for batter consistency)\n";
    std::cout << "1½ teaspoons vanilla extract\n";
    std::cout << "1 cup (150g) fresh or frozen blueberries\n";
    std::cout << "\n";

    // Instructions
    std::cout << "Instructions:\n";
    std::cout << "\n";
    std::cout << "Preheat oven to 400°F (200°C). Line a muffin tin with 12 paper liners.\n";
    std::cout << "In a large bowl, mix dry ingredients: flour, sugar, salt, and baking powder.\n";
    std::cout << "In a small bowl or cup, whisk together: oil, egg, vanilla, and milk.\n";
    std::cout << "Combine wet and dry ingredients. Mix until just combined — don’t overmix!\n";
    std::cout << "Gently fold in blueberries.\n";
    std::cout << "Divide batter evenly into muffin cups (about ¾ full each).\n";
    std::cout << "Bake for 18–22 minutes or until tops are golden and a toothpick comes out clean.\n";
    std::cout << "Let cool for 5–10 minutes, then enjoy!\n";
    std::cout << "\n";
    std::cout << "\n";
    return 0;
}
